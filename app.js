// Prompt perguntando qual o dia da semana
let qualDiaSemana = prompt("Qual é o dia da Semana");

// Se o dia da semana for Sábado ou Domingo vai mostrar a mensagem Bom final de Semana
if (qualDiaSemana == "Sábado" || qualDiaSemana == "Domingo") {
alert("Bom Final de Semana");
} 

// Qualquer outra mensagem mostrará a mensagem boa semana
else {
 alert("Boa Semana");
}

